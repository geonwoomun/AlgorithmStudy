# 보니깐 i = i-1  + i -2 하면 되는 규칙인거 같아서 하니깐 됐다.

def solution(n):
    answer = 0
    dp = [0] * (n + 1)
    dp[0] = 1
    dp[1] = 1
    for i in range(2, n+1):
        dp[i] = dp[i-1] + dp[i-2]

    return dp[n] % 1234567